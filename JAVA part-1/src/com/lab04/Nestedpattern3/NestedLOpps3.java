package com.lab04.Nestedpattern3;

public class NestedLOpps3 {
	 public static void main(String []args){
   	  
    	 for(int i=0;i<=5;i++)
    	 {   System.out.print("\n ");
    		 for(int j=5;j>=0;j--)
    		 {
    			 if(i>=j)
    			 {
                   			 System.out.print("\t*");
    			 }
    			 else
    			 {
    				 System.out.print("\t");
    				 
    			 }
    		}
    			 
    		 
    	 }
     } 
}
