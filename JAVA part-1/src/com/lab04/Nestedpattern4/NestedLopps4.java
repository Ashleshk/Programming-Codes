package com.lab04.Nestedpattern4;

public class NestedLopps4 {
	public static void main(String []args){
	   	  
   	 for(int i=0;i<=5;i++)
   	 {   System.out.print("\n ");
   		 for(int j=0;j<=5;j++)
   		 {
   			 if(j >=i)
   			 {
                  			 System.out.print("\t*");
   			 }
   			 else
   			 {
   				 System.out.print("\t");
   				 
   			 }
   		}
   			 
   		 
   	 }
    } 
}
